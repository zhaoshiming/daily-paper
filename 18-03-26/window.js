// window 对象


window.innerHeight和window.innerWidth属性，
返回网页在当前窗口中可见部分的高度和宽度，
即“视口”（viewport），单位为像素。


window.pageXOffset属性返回页面的水平滚动距离，
window.pageYOffset属性返回页面的垂直滚动距离，单位都为像素



window.scrollTo方法用于将网页的指定位置，
滚动到浏览器左上角。它的参数是相对于整张网页的横坐标和纵坐标。它有一个别名window.scroll。


window.scrollBy方法用于将网页移动指定距离，
单位为像素。它接受两个参数：向右滚动的像素，向下滚动的像素。























































































































